import type { Metadata } from 'next'
import { StyledComponentsRegistry } from '@/UtilsRoot';
import { RootLayoutThemeProvider, ModalProvider, WindowSizeProvider, ReduxProvider } from '@/LibRoot'

import { GlobalStyles, Main } from '@/StylesRoot';
import { Modal, Sidebar, Header } from '@/ComponentsRoot';
export const metadata: Metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app',
}

const getData = async () => {
  try {
    const response = await fetch('http://localhost:3000/api/boards');
    if (!response.ok) throw new Error("sometging went wrong");

    return response.json();

  } catch (error) {
    console.log(error)
  }
}

export default async function RootLayout({
  children,
}: {
  children: React.ReactNode
}) {
  const data = await getData();

  return (
    <html lang="en">
      <body>
        <StyledComponentsRegistry>
          <WindowSizeProvider>
            <RootLayoutThemeProvider>
              <ReduxProvider>
                <ModalProvider>
                  <Header />
                  <Main>
                    <Sidebar boards={data?.boards} />
                    {children}
                  </Main>
                  <Modal />
                  <GlobalStyles />
                </ModalProvider>
              </ReduxProvider>
            </RootLayoutThemeProvider>
          </WindowSizeProvider>
        </StyledComponentsRegistry>
        <div id='modal-root'></div>
      </body>
    </html>
  )
}
